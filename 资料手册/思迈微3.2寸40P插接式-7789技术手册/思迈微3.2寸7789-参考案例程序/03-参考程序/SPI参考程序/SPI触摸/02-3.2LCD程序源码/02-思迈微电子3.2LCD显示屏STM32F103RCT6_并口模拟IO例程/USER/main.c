//=======================================液晶屏数据线接线==========================================//
//16位模式
//DB0-DB15       接PB0-PB15
//8位模式
//DB8-DB15       接PB8-PB15
//=======================================液晶屏控制线接线==========================================//
//LCD_CS	  接PA0	   //片选信号
//LCD_RS	  接PA1	   //寄存器/数据选择信号
//LCD_WR	  接PA2	   //写信号
//LCD_RD	  接PA3	   //读信号
//LCD_RST	  接PA4	   //复位信号
//LCD_LED	  接PA5	   //背光控制信号(高电平点亮)
//=========================================触摸屏触接线=========================================//
//不使用触摸或者模块本身不带触摸，则可不连接
//PEN		    接PA6	   //触摸屏中断信号
//MOS(MOSI)	接PA7	   //SPI总线输出
//MIS(MISO)	接PA8	   //SPI总线输入
//CLK		    接PA9	   //SPI总线时钟
//TCS		    接PA10	 //触摸IC片选
