/*
*********************************************************************************************************
*	函 数 名: LOG_Printf
*	功能说明: 线程安全的串口打印，使用互斥量进行保护
*	形    参: 无
*	返 回 值: 无
*********************************************************************************************************
*/
void LOG_PRINT(char n,char *format, ...)
{
  
#if UART_DEG_EN
  
    char     buf_str[150 + 1];
    va_list   v_args;

    if(n)
    {
        va_start(v_args, format);
        
        (void)vsnprintf((char       *)&buf_str[0],
                        (size_t       ) sizeof(buf_str),
                        (char const *) format,
                        v_args);
         
        va_end(v_args);
        /*获取互斥信号量*/
        xSemaphoreTake(MutexPrint, portMAX_DELAY);
        /*打印输出*/
        printf("%s", buf_str);
        /*释放信号量*/
       xSemaphoreGive(MutexPrint);
    }
    
#endif
  
}



/*
*********************************************************************************************************
*	函 数 名: CoarseDelay
*	功能说明: 粗糙延时
*	形    参: _dly   延时时长
*	返 回 值: NULL
*********************************************************************************************************
*/
void CoarseDelay(unsigned int _dly)
{
	do
	{
		_dly--;
	}
	while(_dly);
}

/*
*********************************************************************************************************
*	函 数 名: Delay_us
*	功能说明: 粗糙延时us
*	形    参: _dly   延时时长
*	返 回 值: NULL
*********************************************************************************************************
*/
void Delay_us(unsigned int _us)
{     
        
        unsigned int delta;
        
        _us = _us * (SysTick->LOAD / (1000000 / configTICK_RATE_HZ));
        
        delta = SysTick->VAL;
        
        if (delta < _us)
        {
          /* wait current OSTick left time gone */
          while (SysTick->VAL < _us);
          
          _us -= delta;
          
          delta = SysTick->LOAD;
        }
        
        while (delta - SysTick->VAL < _us);
        
}	

