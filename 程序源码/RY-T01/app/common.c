/*
*********************************************************************************************************
*	函 数 名: BubbleSort
*	功能说明: 冒泡排序，从小到大
*	形    参: arr数组 ；size 长度
*	返 回 值: NULL
*********************************************************************************************************
*/
void BubbleSort(int *arr,int size)
{
    int i = 0,j = 0;
    int tmp = 0;
    
    for(i=0;i<size-1;i++)
    {
        for(j=0;j<size-i-1;j++)
        {
            if(arr[j] > arr[j+1])
            {
                tmp = arr[j];
                arr[j] = arr[j+1];
                arr[j+1] = tmp;
            }
        }
    }       
}



